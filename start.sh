# Using yarn
# Create a new React app with TypeScript template
yarn create react-app hooks-react --template typescript

# Change directory to the newly created app
cd hooks-react

# Add sass
yarn add sass

# Add react-router-dom
yarn add react-router-dom  

# Add types for react-dom as a development dependency
yarn add @types/react-dom@latest --dev

# Start the development server
yarn start

# Using npm
# Create a new React app with TypeScript template
npx create-react-app hooks-react --template typescript

# Change directory to the newly created app
cd hooks-react

# Add sass
npm install sass

# Add react-router-dom
npm install react-router-dom

# Add types for react-dom as a development dependency
npm install @types/react-dom@latest --save-dev

# Start the development server
npm start